//Regular Function
function functionName(params) { //params are optional
    //Fn Body
}

//Sum function without params
function sum1() {
    //Operations
    return  2 + 3;
}
console.log(sum1()); //Calling function

//Sum function with params
function sum2(firstNumber, secondNumber) {
    return firstNumber + secondNumber;
}

console.log(sum2(5,4)); //9

